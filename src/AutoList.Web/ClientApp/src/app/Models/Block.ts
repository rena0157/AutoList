
/**
 * Simple class that models
 * A block with an Id, Frontage and Area
 */
export class Block {

    /**
     * The Id of the block
     * Usually 'Block 2' for example
     */
    public Id:string;

    /**
     * The Frontage of the block. This can be 0
     */
    public Frontage:number;

    /**
     * The Area of the block. This can be 0
     */
    public Area:number;
}
